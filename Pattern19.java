class Pattern19 {
    public static void main(String[] args) {
        int i,j,n=5;
        for(i=1;i<=n;i++){
            int num =5;
        for(j=1;j<=i;j++){
            System.out.print(num);
            num--;
        }
        System.out.println();
    }
    }    
}
